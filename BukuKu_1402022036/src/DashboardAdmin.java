    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int barisTerpilih = tabelBuku.getSelectedRow();
        if (barisTerpilih == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data buku di tabel yang ingin dihapus terlebih dahulu!");
            return;
        }
    
        String idBuku = tabelBuku.getValueAt(barisTerpilih, 0).toString();
        String judulBuku = tabelBuku.getValueAt(barisTerpilih, 1).toString();
    
        int konfirmasi = JOptionPane.showConfirmDialog(this, 
                "Apakah Anda yakin ingin menghapus buku " + judulBuku + "?", 
                "Konfirmasi Hapus Data", 
                JOptionPane.YES_NO_OPTION, 
                JOptionPane.QUESTION_MESSAGE);
    
        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                Connection konek = DriverManager.getConnection("jdbc:mysql://localhost:3306/tugas5_1402022036", "root", "");
                Statement stat = konek.createStatement();

                String query = "DELETE FROM buku WHERE id_buku = '" + idBuku + "'";

                int berhasil = stat.executeUpdate(query);

                if (berhasil == 1) {
                    JOptionPane.showMessageDialog(this, "Buku '" + judulBuku + "' berhasil dihapus!");

                    loadTabelBuku(); 

                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField5.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jTextField4.setText("");

                    barisKlik = -1; 
                }

                stat.close();
                konek.close();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_deleteActionPerformed

