    private void tabelBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelBukuMouseClicked
        // TODO add your handling code here:
        int baris = tabelBuku.getSelectedRow();
        if (baris == -1) {
            return;
        }

        if (baris == barisKlik) {
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField5.setText("");
            jComboBox1.setSelectedIndex(0);
            jTextField4.setText(""); 
        
            tabelBuku.clearSelection();
            barisKlik = -1;
        
        } else { 
            jTextField1.setText(tabelBuku.getValueAt(baris, 1).toString());
            jTextField2.setText(tabelBuku.getValueAt(baris, 2).toString());
            jTextField3.setText(tabelBuku.getValueAt(baris, 3).toString());
            jTextField5.setText(tabelBuku.getValueAt(baris, 4).toString());

            String kategori = tabelBuku.getValueAt(baris, 5).toString();
            jComboBox1.setSelectedItem(kategori);

            jTextField4.setText(tabelBuku.getValueAt(baris, 6).toString());
            
            barisKlik = baris;
        }
    }//GEN-LAST:event_tabelBukuMouseClicked

