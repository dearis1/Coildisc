    public void loadTabelBuku() {
        DefaultTableModel model = (DefaultTableModel) tabelBuku.getModel();
        model.setRowCount(0);
        
        try {
            Connection konek = DriverManager.getConnection("jdbc:mysql://localhost:3306/tugas5_1402022036", "root", "");
            Statement stat = konek.createStatement();
            String query = "SELECT * FROM buku";
            ResultSet rs = stat.executeQuery(query);
            
            while (rs.next()) {
                Object[] dataBuku = {
                    rs.getInt("id_buku"),
                    rs.getString("judul_buku"),
                    rs.getString("penulis"),
                    rs.getString("penerbit"),
                    rs.getInt("tahun_terbit"),
                    rs.getString("kategori"),
                    rs.getInt("stok")
                };
                model.addRow(dataBuku);
            }
            
            rs.close();
            stat.close();
            konek.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data tabel: " + e.getMessage());
        }
    }
