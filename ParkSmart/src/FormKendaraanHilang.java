    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (txtKode.getText().trim().isEmpty() || txtPlat.getText().trim().isEmpty() || dcTglLapor.getDate() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Kode Karcis, Plat Nomor, dan Tanggal Lapor wajib diisi lek!", "Validasi Gagal", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            java.sql.Connection conn = Koneksi.getConnection(); 
            
            String sql = "INSERT INTO kendaraan_hilang (kode_tarif, plat_nomor, jenis_kendaraan, warna, merk, no_telp, tanggal_lapor, jam_lapor, status) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            java.sql.PreparedStatement ps = conn.prepareStatement(sql);
            
            ps.setString(1, txtKode.getText().trim().toUpperCase());
            ps.setString(2, txtPlat.getText().trim().toUpperCase());
            ps.setString(3, cbJenis.getSelectedItem().toString());
            ps.setString(4, txtWarna.getText().trim());
            ps.setString(5, txtMerk.getText().trim());
            ps.setString(6, txtTlp.getText().trim());
            
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            String tglLaporFormatted = sdf.format(dcTglLapor.getDate());
            ps.setString(7, tglLaporFormatted);
            ps.setString(8, txtJam.getText().trim());
            ps.setString(9, "Belum Ditemukan");

            int sukses = ps.executeUpdate();
            if (sukses > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Laporan kendaraan hilang berhasil disimpan!", "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                
                if (panelLaporanUtama != null) {
                    panelLaporanUtama.cbJenisLaporan.setSelectedIndex(4);
                    panelLaporanUtama.tampilkanDataLaporan();
                }
                
                this.dispose();
            }
            ps.close();
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal simpan ke database: " + ex.getMessage(), "Eror", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

